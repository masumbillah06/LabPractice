public class Person {
    private String name;
    private int id;
    public String getname(){
        return name;
    }
    public String setname(String n){
        this.name = n;
        return name;
    }
    public int getid(){
        return id;
    }
    public int setid(int x){
        this.id = x;
        return id;
    }
}
